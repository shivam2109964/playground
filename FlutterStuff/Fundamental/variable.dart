void main() {
  // Declaration
  int a;
  // Declaration + Initialization
  int b = 10;
  //  assignment
  a = 120;

  int c; // Declaration
  c = 120; // Initialization (first assignment)
  c = 200; // Reassignment
  c = 300; // Reassignment

  int d = 10; // Declaration + Initialization
  d = 50; // Reassignment

  print("$a, $b, $c, $d");
}
